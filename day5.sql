# SQLZoo Solutions: SELECT within SELECT

This repository contains my completed solutions for the **SELECT within SELECT** tutorial on SQLZoo. These exercises focus on practicing subqueries, correlated subqueries, and operators like `ALL`.

## Key Concepts Practiced:
* **Nested Queries:** Using a query inside a `WHERE` clause to filter dynamically.
* **Correlated Subqueries:** Referencing columns from the outer query inside the inner query.
* **The ALL Operator:** Comparing values against an entire set returned by a subquery.

## Exercises Covered:
6. Bigger than every country in Europe
7. Largest country (by area) in each continent
8. First country of each continent (alphabetically)
9. Continents where all countries have a population <= 25,000,000
10. Countries with populations three times bigger than all their neighbors
